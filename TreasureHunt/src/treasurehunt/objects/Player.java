package treasurehunt.objects;

public class Player {

	private int xpos;
	private int ypos;
	private int hintCount = 3;
	
	
	public int getXpos() {
		return xpos;
	}
	public void setXpos(int xpos) {
		this.xpos = xpos;
	}
	public int getYpos() {
		return ypos;
	}
	public void setYpos(int ypos) {
		this.ypos = ypos;
	}
	public int getHintCount() {
		return hintCount;
	}
	
	
	
}
